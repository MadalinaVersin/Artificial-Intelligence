class Nod:
    def __init__(self, info, h):
        self.info = info  # este o lista de stive(liste)
        self.h = h  # h este numarul de blocuri(cuburi) care nu sunt asezate la locul lor

    def __str__(self):
        return "({}, h={})".format(self.info, self.h)

    def __repr__(self):
        return f"({self.info}, h={self.h})"


def fct_h(lista_curenta, lista_scop):
    """ folosim dictionare pentru a compara diferentele dintre cele doua liste de liste """
    dictionar_lista_curenta = {}
    dictionar_lista_scop = {}
    h = 0
    """ introducem cuburile din lista curenta in dictionar,cheia fiind litera cubului,
    iar valoarea fiind perechea formata din pozitia pe care se afla acea litera in lista de stive
    """
    for stiva in range(len(lista_curenta)):
        for cub in range(len(lista_curenta[stiva])):
            dictionar_lista_curenta[lista_curenta[stiva][cub]] = (stiva, cub)

    """introducem cuburile din lista curenta in dictionar,cheia fiind litera cubului,
    iar valoarea fiind perechea formata din pozitia pe care se afla acea litera in lista de stive
    """
    for stiva in range(len(lista_scop)):
        for cub in range(len(lista_scop[stiva])):
            dictionar_lista_scop[lista_scop[stiva][cub]] = (stiva, cub)

    """cautam diferentele dintre cele doua liste prin compararea  valorilor de la aceasi cheie din  cele doua dictionare
    daca valorile sunt diferite se mareste h-ul"""

    for valuare in dictionar_lista_curenta:
        if dictionar_lista_curenta[valuare] != dictionar_lista_scop[valuare]:
            h = h + 1

    return h


class Problema:
    def __init__(self):
        """nodul_start find lista de la care plecam , acesta  este de tip nod """
        self.nod_start = Nod([['a'], ['c', 'b'], ['d']],
                             fct_h([['a'], ['c', 'b'], ['d']], [['b', 'c'], ['d', 'a']]))  # de tip Nod
        """nodul scop fiind lista la care vrem sa ajungem, acesta fiind doar o lista """
        self.nod_scop = [['b', 'c'], [], ['d', 'a']]  # doar info (fara h)


